package Loops;

public class C01 {
    public static void main(String[] args) {


        // FOR

            for (int i=1; i<=10; i++){
                System.out.println(i);
            }

        System.out.println("for dongusu bitti");


        // WHİLE

        int i=1;

        while (i<10){
            System.out.println(i);
            i++;
        }

        System.out.println("while dongusu bitti");

        // DO WHİLE

        int j=1;
        do{
            System.out.println(j);
            j+=2;
        }while (j<10);









    }
}
